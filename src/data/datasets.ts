export function getDatasetBySlug(slug: string): Dataset | undefined {
  return datasets.find((d) => d.slug === slug);
}

export function getRelatedDatasets(dataset: Dataset): Dataset[] {
  return dataset.pairsWith
    .map((slug) => getDatasetBySlug(slug))
    .filter((d): d is Dataset => Boolean(d));
}

export function getFeaturedDatasets(): Dataset[] {
  return datasets.filter((d) => d.flags?.includes("good-starting"));
}

