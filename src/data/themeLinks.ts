export function themesFromCategoryList(categories: string[]): ClusterId[] {
  const out: ClusterId[] = [];
  for (const c of categories) {
    const t = CATEGORY_TO_THEME[c] ?? CATEGORY_TO_THEME[c.toLowerCase()];
    if (t) out.push(normalizeClusterId(t));
  }
  return out;
}
