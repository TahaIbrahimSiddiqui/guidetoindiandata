function withSummary(d: DatasetDraft): Dataset {
  const summary =
    d.summary?.trim() ||
    DATASET_SUMMARIES[d.slug] ||
    `${d.shortTitle} is used for research and analysis on ${d.categories.slice(0, 3).join(", ") || "Indian data"}.`;
  return { ...d, summary };
}

